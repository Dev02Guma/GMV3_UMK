package com.app.gmv3.models;

public class Vineta {
    String mFactura;
    String mFecha;
    String mVineta;
    String mCantidad;
    String mValor;
    String mTotal;
    String mLinea;

    public String getmFactura() {
        return mFactura;
    }

    public String getmFecha() {
        return mFecha;
    }

    public String getmVineta() {
        return mVineta;
    }

    public String getmCantidad() {
        return mCantidad;
    }

    public String getmValor() {
        return mValor;
    }

    public String getmTotal() {
        return mTotal;
    }

    public String getmLinea() {
        return mLinea;
    }
}
